package com.yadishot.tuchgram.Models;

public class InviteModel {
    String projectInviter;
    String projectDecs;
    String projectAccept;
    String projectMoney;
    String projectSod;

    public InviteModel() {
    }

    public String getProjectInviter() {
        return projectInviter;
    }

    public void setProjectInviter(String projectInviter) {
        this.projectInviter = projectInviter;
    }

    public String getProjectDecs() {
        return projectDecs;
    }

    public void setProjectDecs(String projectDecs) {
        this.projectDecs = projectDecs;
    }

    public String getProjectAccept() {
        return projectAccept;
    }

    public void setProjectAccept(String projectAccept) {
        this.projectAccept = projectAccept;
    }

    public String getProjectMoney() {
        return projectMoney;
    }

    public void setProjectMoney(String projectMoney) {
        this.projectMoney = projectMoney;
    }

    public String getProjectSod() {
        return projectSod;
    }

    public void setProjectSod(String projectSod) {
        this.projectSod = projectSod;
    }
}
